#!/usr/bin/with-contenv bashio
# ==============================================================================
# Stage the Claude Code environment + a sensible default settings.json.
#
# ~/.claude_env carries:
#   - SUPERVISOR_TOKEN (for /api/* via http://supervisor and the ha-* helpers)
#   - ANTHROPIC_API_KEY or CLAUDE_CODE_OAUTH_TOKEN (auth for Claude Code)
#   - CLAUDE_CONFIG_DIR (so settings.json / /login state live under /data)
#   - CLAUDE_CODE_EFFORT_LEVEL (if claude.effort is set — env var rather than
#     settings.json's effortLevel, because the env var also overrides Opus
#     4.7's one-time "launch effort" pin, which the settings key does not)
#   - USE_BUILTIN_RIPGREP
# It is sourced by ~/.bashrc (interactive shells) AND by
# /etc/profile.d/01-claude-env.sh (all login shells, including the
# non-interactive `bash -lc` an iOS Shortcut / cron uses to launch claude --rc),
# so headless launches get the same config dir, auth, and $SUPERVISOR_TOKEN as
# an interactive login.
# ==============================================================================
set -e

declare username
declare anthropic_api_key
declare oauth_token
declare permission_mode
declare model
declare effort

username=$(bashio::config 'ssh.username')
permission_mode=$(bashio::config 'claude.permission_mode' 'default')

HOME_DIR="/data/home"
ENV_FILE="${HOME_DIR}/.claude_env"

# ------------------------------------------------------------------------------
# Build the env file from add-on options + Supervisor-injected env
# ------------------------------------------------------------------------------
{
    echo "# Auto-generated by Claude Code add-on; do not hand-edit."
    echo "# Sourced by ~/.bashrc (interactive) and /etc/profile.d/01-claude-env.sh"
    echo "# (all login shells, incl. the headless 'bash -lc' that launches claude)."
    echo
    echo "export CLAUDE_CONFIG_DIR=/data/claude"
    echo "export USE_BUILTIN_RIPGREP=0"

    if bashio::config.has_value 'claude.anthropic_api_key'; then
        anthropic_api_key=$(bashio::config 'claude.anthropic_api_key')
        echo "export ANTHROPIC_API_KEY='${anthropic_api_key}'"
    fi

    if bashio::config.has_value 'claude.oauth_token'; then
        oauth_token=$(bashio::config 'claude.oauth_token')
        echo "export CLAUDE_CODE_OAUTH_TOKEN='${oauth_token}'"
    fi

    if bashio::config.has_value 'claude.effort'; then
        effort=$(bashio::config 'claude.effort')
        # 'default' is a sentinel meaning "let the model decide" — skip the
        # env var so Claude Code's own default kicks in.
        if [[ "${effort}" != "default" ]]; then
            echo "export CLAUDE_CODE_EFFORT_LEVEL='${effort}'"
        fi
    fi

    if [[ -n "${SUPERVISOR_TOKEN:-}" ]]; then
        echo "export SUPERVISOR_TOKEN='${SUPERVISOR_TOKEN}'"
    fi
} > "${ENV_FILE}"
chmod 600 "${ENV_FILE}"
chown "${username}:${username}" "${ENV_FILE}"

# ------------------------------------------------------------------------------
# Stage a default Claude Code settings.json under CLAUDE_CONFIG_DIR. Only
# written if missing — once the operator customises it, we leave it alone.
# ------------------------------------------------------------------------------
SETTINGS_FILE="/data/claude/settings.json"
if [[ ! -f "${SETTINGS_FILE}" ]]; then
    cat > "${SETTINGS_FILE}" <<EOF
{
  "defaultMode": "${permission_mode}",
  "permissions": {
    "allow": [
      "Read",
      "Edit",
      "Write",
      "Bash(curl http://supervisor/*)",
      "Bash(curl -* http://supervisor/*)",
      "Bash(ha-state *)",
      "Bash(ha-call-service *)",
      "Bash(ha-list-entities *)",
      "Bash(ha-check-config)",
      "Bash(ha-restart)",
      "Bash(ha-token)",
      "Bash(sqlite3 -readonly /homeassistant/home-assistant_v2.db *)",
      "Bash(jq *)"
    ]
  }
}
EOF
    bashio::log.info "Default Claude settings.json staged"
fi

# ------------------------------------------------------------------------------
# If claude.model is set, keep settings.json's "model" key in sync with it on
# every start (unlike permission_mode / the rest of settings.json, which are
# only written when the file is first staged). An empty option leaves whatever
# "model" is already there — including a hand-set value — untouched.
# ------------------------------------------------------------------------------
if bashio::config.has_value 'claude.model'; then
    model=$(bashio::config 'claude.model')
    if jq -e . "${SETTINGS_FILE}" >/dev/null 2>&1; then
        tmp=$(mktemp)
        jq --arg m "${model}" '.model = $m' "${SETTINGS_FILE}" > "${tmp}" \
            && mv "${tmp}" "${SETTINGS_FILE}"
        bashio::log.info "Claude model pinned to ${model} in settings.json"
    else
        bashio::log.warning "settings.json is not valid JSON; not setting model"
    fi
fi

# Make sure the operator owns the config dir
chown -R "${username}:${username}" /data/claude

bashio::log.info "Claude environment configured for ${username} (mode: ${permission_mode})"
